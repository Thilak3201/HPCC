name = "thilakkumar"

print(name)

print(name[1])        # Value printed  based on index

print(name[0:6])      # the start with 0 and up to 5th number

print(name[-1])       # -1 indicated print values in reverse order

print(name[-6:-1])     # -1 start with last element & it is not include last number

print(name[::-1])       # Reverse a string

print(name[0:9:2])      # steps 2 (jumpoes spaces 2 space)